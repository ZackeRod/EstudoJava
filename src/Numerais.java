public class Numerais {

    public static void main(String[] args) {

        // Tipos Numerais Primitivos
        double valor1 = 0.5;   // Valor com ponto flutuante
        int valor2 = 25;   // Valor inteiro
        long valor3 = 1000000000;  // Mais casas decimais
        float valor4 = 10.50F; // ponto flutuante float


        System.out.println(valor1 + valor2);
    }
}
